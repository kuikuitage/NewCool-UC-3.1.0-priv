void iso8859_to_unicode(u32 code,
                        u8 *p_iso8859,
                        s32 length,
                        u16 *p_unicode,
                        s32 maxlen)
{
  s32 si, di;

  CHECK_FAIL_RET_VOID(NULL != p_iso8859
         && 0 != length
         && NULL != p_unicode
         && 0 != maxlen);

  for(si = di = 0; (si < length) && (di < (maxlen -1)); si++)
  {
    if(p_iso8859[si] < 0x80)
    {
      p_unicode[di++] = (u16)(p_iso8859[si]);
    }
    else if(p_iso8859[si] <= 0x9F)
    {
      continue;
    }
    else
    {
      if(iso8859_map[code] != NULL)
      {
        p_unicode[di++] = iso8859_map[code][p_iso8859[si] - 0xA0];
      }
      else if(code == 1)  /* ISO-8859-1 is ASCII */
      {
        p_unicode[di++] = (u16)(p_iso8859[si]);
      }
      else if(code == 5)      /* ISO-8859-5 has some rules to follow */
      {
        if((p_iso8859[si] == 0xA0) || (p_iso8859[si] == 0xAD))
        {
          p_unicode[di++] = (u16)(p_iso8859[si]);
        }
        else if(p_iso8859[si] == 0xF0)
        {
          p_unicode[di++] = (u16)(0x2116);
        }
        else if(p_iso8859[si] == 0xFD)
        {
          p_unicode[di++] = (u16)(0x00A7);
        }
        else
        {
          p_unicode[di++] = (u16)(p_iso8859[si] + 0x360);
        }
      }
    }
  }
  p_unicode[di] = 0x0000;
}
